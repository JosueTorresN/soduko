    private void jButtonVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerificarActionPerformed
        GuardarCambio();
        juegoActual.verificaciones++;
        ArrayList<String> matriz = new ArrayList();
        for (JTextField textField : tableroTF){
            if (textField.getText().equals("") || textField.getText().equals(" ")){
                matriz.add("0");
            } else {
                matriz.add(textField.getText());
            }
        }
        String[] temp = verificarTablero(matriz.toString(),juegoActual. matrizCompleta.toString());
        ArrayList<String> lista = new ArrayList();
        lista.addAll(Arrays.asList(temp));
        if (lista.toString().equals("[]")){
            JOptionPane.showMessageDialog(this,"¡Felicidades ha ganado!" , "Partida Finalizada",1);                
            jButtonSugerencia.setEnabled(false);
            jButtonVerificar.setEnabled(false);
            jButtonReiniciar.setEnabled(false);
            jButtonAutoSolucion.setEnabled(false);
            juegoActual.estado = 1;
            activar = false;
            t.stop();
            juegoActual.cronometro = cronometro;
            historialJuegos.add(juegoActual);
        }else{
            jLabelErrores.setText(lista.get(0));
            jLabelVacios.setText(lista.get(1));
            jLabelCorrectas.setText(lista.get(2));
            juegoActual.errores += Integer.parseInt(lista.get(0));
        }
    }//GEN-LAST:event_jButtonVerificarActionPerformed

