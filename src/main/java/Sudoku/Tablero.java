    private void jButtonGJuegoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGJuegoActionPerformed
        jLabelMensajes.setVisible(false);
        jButtonCargarRepeticion.setEnabled(true);
        jButtonGuardarRepeticion.setEnabled(true);
        jButtonReiniciar.setEnabled(true);
        jButtonSugerencia.setEnabled(true);
        jButtonVerificar.setEnabled(true);
        jButtonAutoSolucion.setEnabled(true);
        jButtonAutoSolucion.setVisible(true);
        jLabelSugerencias.setText("5");
        jLabelErrores.setText(" ");
        jLabelVacios.setText(" ");
        jLabelCorrectas.setText(" ");
        jLabelSugerencias.setText("5");
        if (juegoActual != null){
            if(juegoActual.estado == 0){
                juegoActual.estado = 3;
                activar = false;
                t.stop();
                juegoActual.cronometro = cronometro;
                historialJuegos.add(juegoActual);
            }
        }        
        juegoActual = new Juego();
        
        String cadena = obtenerMatrixJuego();
        if (cadena.equals("")){
            System.out.println("Error: No se pudo obtener una matriz para realizar la partida.");
            return ;
        }
        cadena = cadena.replace("]", "");
        cadena = cadena.replace("[", "");
        cadena = cadena.replace(" ", "");
        String[] temp = cadena.split(",");
        juegoActual.matrizCompleta.addAll(Arrays.asList(temp));  
        int pos = 0;
        System.out.print("Completa: " + juegoActual.matrizCompleta.toString() + "\n");
        for(int i = 0; i<9;i++){
            for(int j = 0; j<9;j++){
                System.out.print(temp[pos]+ "  ");
                pos++;
            }
            System.out.print("\n");
        }
        ArrayList<String> matrizTemp = new ArrayList(juegoActual.matrizCompleta);
        ArrayList<String> matrizInicio = crearPistas (matrizTemp);
        contarEspaciosVacios(matrizInicio);
       
        juegoActual.historialTablero.add (matrizInicio);
        int i = 0;
        for (JTextField textField : tableroTF){
            textField.setEditable(true);
            textField.setBackground(Color.WHITE);
            if (matrizInicio.get(i).equals("0")){
                textField.setText("");
                textField.setEnabled(true);
            } else {
                textField.setText(matrizInicio.get(i));
                textField.setEnabled(false);
                textField.setFont(new java.awt.Font("Tahoma", Font.BOLD, 11));
            }
            i++;
        }
        if (jLabelSugerencias.getText().equals("0")){
            jLabelSugerencias.setText("5");
        }
        jButtonSugerencia.setEnabled(true);
        centesima =00;
        segundo = 00;
        minuto =00;
        hora =00;
        empezar();
        t.start();
        juegoActual.cronometro = cronometro;
    }//GEN-LAST:event_jButtonGJuegoActionPerformed

