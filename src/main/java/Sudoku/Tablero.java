    private void jButtonGuardarRepeticionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarRepeticionActionPerformed
        FileWriter fichero = null;
        PrintWriter pw = null;
        try{
            File file = new File("repeticion.txt");
            // Si el archivo no existe es creado
            if (!file.exists()) {
                file.createNewFile();
            }
            fichero = new FileWriter("repeticion.txt");
            pw = new PrintWriter(fichero);
            for (ArrayList<String> m : juegoActual.historialTablero){
                pw.println(m);
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
           try {
           // Nuevamente aprovechamos el finally para 
           // asegurarnos que se cierra el fichero.
           if (null != fichero)
              fichero.close();
           } catch (Exception e2) {
              e2.printStackTrace();
           }
        }
    }//GEN-LAST:event_jButtonGuardarRepeticionActionPerformed

